// Combined vocabulary for general use
export const vocabularyWords: Word[] = [...dutchToGermanWords, ...germanToDutchWords];

// Helper functions to work with vocabulary
export const getWordById = (id: string): Word | undefined => {
  return vocabularyWords.find(word => word.id === id);
};

export const getWordsByCategory = (category: string): Word[] => {
  return vocabularyWords.filter(word => word.category === category);
};

export const getWordsByDifficulty = (difficulty: 'beginner' | 'intermediate' | 'advanced'): Word[] => {
  return vocabularyWords.filter(word => word.difficulty === difficulty);
};

export const getRandomWords = (count: number, direction: 'dutch-to-german' | 'german-to-dutch' = 'dutch-to-german'): Word[] => {
  const wordList = direction === 'dutch-to-german' ? dutchToGermanWords : germanToDutchWords;
  const shuffled = [...wordList].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};

export const getRandomWordsExcept = (count: number, excludeIds: string[], direction: 'dutch-to-german' | 'german-to-dutch' = 'dutch-to-german'): Word[] => {
  const wordList = direction === 'dutch-to-german' ? dutchToGermanWords : germanToDutchWords;
  const available = wordList.filter(word => !excludeIds.includes(word.id));
  const shuffled = [...available].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};

export const getRandomOptionsForWord = (word: Word, optionCount: number, direction: 'dutch-to-german' | 'german-to-dutch' = 'dutch-to-german'): string[] => {
  // Get other words to use as incorrect options
  const otherWords = getRandomWordsExcept(optionCount - 1, [word.id], direction);
  
  // Extract the translations based on the direction
  let correctAnswer, incorrectOptions;
  
  if (direction === 'dutch-to-german') {
    correctAnswer = word.german;
    incorrectOptions = otherWords.map(w => w.german);
  } else {
    correctAnswer = word.dutch;
    incorrectOptions = otherWords.map(w => w.dutch);
  }
  
  // Combine and shuffle
  const options = [correctAnswer, ...incorrectOptions];
  return options.sort(() => 0.5 - Math.random());
};

export default vocabularyWords;
